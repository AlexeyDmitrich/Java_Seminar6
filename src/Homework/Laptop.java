package Homework;

public class Laptop {
    private String model;
    private String modelName;
    private int ram;
    private int memory;
    private String os;
    private String color;

    public Laptop(String model, String modelName, int ram, int memory, String os, String color) {
        this.model = model;
        this.modelName = modelName;
        this.ram = ram;
        this.memory = memory;
        this.os = os;
        this.color = color;
    }
}
